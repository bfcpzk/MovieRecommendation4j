package model;

public class MovieSimilarity {
	private int maid;
	private int mbid;
	private int sim_score;

	public int getMaid() {
		return maid;
	}

	public void setMaid(int maid) {
		this.maid = maid;
	}

	public int getMbid() {
		return mbid;
	}

	public void setMbid(int mbid) {
		this.mbid = mbid;
	}

	public int getSim_score() {
		return sim_score;
	}

	public void setSim_score(int sim_score) {
		this.sim_score = sim_score;
	}

}
